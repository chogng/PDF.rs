import { readFile, writeFile } from "node:fs/promises";
import { fileURLToPath } from "node:url";

import {
  NATIVE_WORKER_ABI_SHA256,
  NATIVE_WORKER_ABI_VERSION,
  NATIVE_WORKER_ABI_WORDS,
  NATIVE_WORKER_FUNCTION_SIGNATURES,
  NATIVE_WORKER_POLL_MASK,
  NATIVE_WORKER_POLL_OUTPUT,
  NATIVE_WORKER_POLL_PENDING,
  NATIVE_WORKER_STATUS_INTERNAL_UNWIND,
  NATIVE_WORKER_STATUS_OK,
  NATIVE_WORKER_STATUS_REJECTED,
} from "./native-worker-contract.mjs";

const output = fileURLToPath(
  new URL("../src/native-worker-abi.generated.ts", import.meta.url),
);
const signatureEntries = Object.entries(NATIVE_WORKER_FUNCTION_SIGNATURES)
  .map(([name, signature]) =>
    `  ${JSON.stringify(name)}: ${JSON.stringify(signature)},`)
  .join("\n");
const source = `// Generated by scripts/generate-native-worker-abi.mjs. Do not edit.\n\nexport const NATIVE_WORKER_ABI_VERSION = ${NATIVE_WORKER_ABI_VERSION};\nexport const NATIVE_WORKER_ABI_SHA256 = ${JSON.stringify(NATIVE_WORKER_ABI_SHA256)};\nexport const NATIVE_WORKER_ABI_HASH_WORDS = Object.freeze([\n${NATIVE_WORKER_ABI_WORDS.map((word) => `  0x${word.toString(16).padStart(8, "0")},`).join("\n")}\n] as const);\nexport const NATIVE_WORKER_STATUS_OK = ${NATIVE_WORKER_STATUS_OK};\nexport const NATIVE_WORKER_STATUS_REJECTED = 0x${NATIVE_WORKER_STATUS_REJECTED.toString(16)};\n// Reserved for unwind-capable/internal builds. Production panic=abort traps.\nexport const NATIVE_WORKER_STATUS_INTERNAL_UNWIND = 0x${NATIVE_WORKER_STATUS_INTERNAL_UNWIND.toString(16)};\nexport const NATIVE_WORKER_POLL_OUTPUT = ${NATIVE_WORKER_POLL_OUTPUT};\nexport const NATIVE_WORKER_POLL_PENDING = ${NATIVE_WORKER_POLL_PENDING};\nexport const NATIVE_WORKER_POLL_MASK = ${NATIVE_WORKER_POLL_MASK};\nexport const NATIVE_WORKER_FUNCTION_SIGNATURES = Object.freeze({\n${signatureEntries}\n} as const);\n`;

if (process.argv.includes("--check")) {
  let existing;
  try {
    existing = await readFile(output, "utf8");
  } catch {
    throw new Error("generated Native Worker ABI source is missing");
  }
  if (existing !== source) {
    throw new Error("generated Native Worker ABI source is stale");
  }
} else {
  await writeFile(output, source);
}
