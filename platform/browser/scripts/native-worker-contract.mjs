const canonicalAbiDescription = Object.freeze({
  version: NATIVE_WORKER_ABI_VERSION,
  functions: NATIVE_WORKER_FUNCTION_SIGNATURES,
});

export const sha256 = (bytes) =>
  createHash("sha256").update(bytes).digest("hex");

export const NATIVE_WORKER_ABI_SHA256 = sha256(
  new TextEncoder().encode(JSON.stringify(canonicalAbiDescription)),
);
export const NATIVE_WORKER_ABI_WORDS = Object.freeze(
  NATIVE_WORKER_ABI_SHA256.match(/.{8}/gu).map((chunk) =>
    Number.parseInt(
      `${chunk.slice(6, 8)}${chunk.slice(4, 6)}${chunk.slice(2, 4)}${chunk.slice(0, 2)}`,
      16,
    ),
  ),
);
