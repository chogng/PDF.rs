const isSafePositiveInteger = (value) =>
  Number.isSafeInteger(value) && value > 0;

export const renderNativeWorkerGlue = (artifact) => {
  if (
    typeof artifact !== "object"
    || artifact === null
    || !isSafePositiveInteger(artifact.byteLength)
    || typeof artifact.sha256 !== "string"
    || !/^[0-9a-f]{64}$/u.test(artifact.sha256)
    || !isSafePositiveInteger(artifact.minimumMemoryPages)
    || artifact.maximumMemoryPages !== MAX_WASM_PAGES
    || artifact.minimumMemoryPages > artifact.maximumMemoryPages
  ) {
    throw new Error("invalid Native Worker glue artifact");
  }
  return `// Generated by scripts/build-native-worker.mjs. Do not edit.
export const NATIVE_WORKER_ARTIFACT = Object.freeze({
  url: new URL("./engine.wasm", import.meta.url),
  byteLength: ${artifact.byteLength},
  sha256: "${artifact.sha256}",
  minimumMemoryPages: ${artifact.minimumMemoryPages},
  maximumMemoryPages: ${artifact.maximumMemoryPages},
});

export const createNativeWorkerEngineLoader = (Loader, runtime) => {
  if (typeof Loader !== "function") {
    throw new TypeError("BrowserNativeWorkerLoader constructor is required");
  }
  return runtime === undefined
    ? new Loader(NATIVE_WORKER_ARTIFACT)
    : new Loader(NATIVE_WORKER_ARTIFACT, runtime);
};
`;
};

const readU32Leb = (bytes, cursor, end) => {
  let result = 0;
  for (let shift = 0; shift < 35; shift += 7) {
    if (cursor.value >= end) {
      throw new Error("truncated Wasm LEB");
    }
    const byte = bytes[cursor.value];
    cursor.value += 1;
    if (shift === 28 && (byte & 0xf0) !== 0) {
      throw new Error("overflowing Wasm LEB");
    }
    result |= (byte & 0x7f) << shift;
    if ((byte & 0x80) === 0) {
      return result >>> 0;
    }
  }
  throw new Error("overflowing Wasm LEB");
};

const readWasmName = (bytes, cursor, end) => {
  const length = readU32Leb(bytes, cursor, end);
  const nameEnd = cursor.value + length;
  if (length > 256 || !Number.isSafeInteger(nameEnd) || nameEnd > end) {
    throw new Error("invalid Wasm export name");
  }
  const name = new TextDecoder("utf-8", { fatal: true }).decode(
    bytes.subarray(cursor.value, nameEnd),
  );
  cursor.value = nameEnd;
  return name;
};

const wasmValueType = (value) => {
  switch (value) {
    case 0x7f: return "i32";
    case 0x7e: return "i64";
    case 0x7d: return "f32";
    case 0x7c: return "f64";
    case 0x7b: return "v128";
    case 0x70: return "funcref";
    case 0x6f: return "externref";
    default: throw new Error("invalid Wasm value type");
  }
};

export const inspectNativeWorkerAbi = (bytes) => {
  if (
    bytes.byteLength < 8
    || bytes[0] !== 0 || bytes[1] !== 0x61 || bytes[2] !== 0x73
    || bytes[3] !== 0x6d || bytes[4] !== 1 || bytes[5] !== 0
    || bytes[6] !== 0 || bytes[7] !== 0
  ) {
    throw new Error("invalid Wasm header");
  }
  const cursor = { value: 8 };
  const signatures = [];
  const functionTypes = [];
  const functionExports = new Map();
  while (cursor.value < bytes.byteLength) {
    const sectionId = bytes[cursor.value];
    cursor.value += 1;
    const sectionLength = readU32Leb(bytes, cursor, bytes.byteLength);
    const sectionEnd = cursor.value + sectionLength;
    if (!Number.isSafeInteger(sectionEnd) || sectionEnd > bytes.byteLength) {
      throw new Error("invalid Wasm section");
    }
    if (sectionId === 1) {
      const count = readU32Leb(bytes, cursor, sectionEnd);
      if (count > 65_536) throw new Error("too many Wasm types");
      for (let index = 0; index < count; index += 1) {
        if (bytes[cursor.value] !== 0x60) throw new Error("invalid Wasm type");
        cursor.value += 1;
        const parameterCount = readU32Leb(bytes, cursor, sectionEnd);
        if (parameterCount > 64) throw new Error("too many Wasm parameters");
        const parameters = [];
        for (let parameter = 0; parameter < parameterCount; parameter += 1) {
          if (cursor.value >= sectionEnd) throw new Error("truncated Wasm type");
          parameters.push(wasmValueType(bytes[cursor.value]));
          cursor.value += 1;
        }
        const resultCount = readU32Leb(bytes, cursor, sectionEnd);
        if (resultCount > 16) throw new Error("too many Wasm results");
        const results = [];
        for (let result = 0; result < resultCount; result += 1) {
          if (cursor.value >= sectionEnd) throw new Error("truncated Wasm type");
          results.push(wasmValueType(bytes[cursor.value]));
          cursor.value += 1;
        }
        signatures.push(`(${parameters.join(",")})->${results.length === 0 ? "void" : results.join(",")}`);
      }
      if (cursor.value !== sectionEnd) throw new Error("invalid Wasm type section");
    } else if (sectionId === 2) {
      if (readU32Leb(bytes, cursor, sectionEnd) !== 0 || cursor.value !== sectionEnd) {
        throw new Error("Native Worker Wasm must have zero imports");
      }
    } else if (sectionId === 3) {
      const count = readU32Leb(bytes, cursor, sectionEnd);
      if (count > 1_000_000) throw new Error("too many Wasm functions");
      for (let index = 0; index < count; index += 1) {
        functionTypes.push(readU32Leb(bytes, cursor, sectionEnd));
      }
      if (cursor.value !== sectionEnd) throw new Error("invalid Wasm function section");
    } else if (sectionId === 7) {
      const count = readU32Leb(bytes, cursor, sectionEnd);
      if (count > 256) throw new Error("too many Wasm exports");
      for (let index = 0; index < count; index += 1) {
        const name = readWasmName(bytes, cursor, sectionEnd);
        if (cursor.value >= sectionEnd) throw new Error("truncated Wasm export");
        const kind = bytes[cursor.value];
        cursor.value += 1;
        const itemIndex = readU32Leb(bytes, cursor, sectionEnd);
        if (kind === 0) {
          if (functionExports.has(name)) throw new Error("duplicate Wasm function export");
          functionExports.set(name, itemIndex);
        }
      }
      if (cursor.value !== sectionEnd) throw new Error("invalid Wasm export section");
    }
    cursor.value = sectionEnd;
  }
  const actual = {};
  for (const [name, expected] of Object.entries(NATIVE_WORKER_FUNCTION_SIGNATURES)) {
    const functionIndex = functionExports.get(name);
    const typeIndex = functionIndex === undefined ? undefined : functionTypes[functionIndex];
    if (signatures[typeIndex] !== expected) {
      throw new Error(`Native Worker Wasm signature drifted for ${name}`);
    }
    actual[name] = expected;
  }
  return Object.freeze(actual);
};

export const inspectNativeWorkerMemory = (bytes) => {
  if (
    bytes.byteLength < 8
    || bytes[0] !== 0
    || bytes[1] !== 0x61
    || bytes[2] !== 0x73
    || bytes[3] !== 0x6d
    || bytes[4] !== 1
    || bytes[5] !== 0
    || bytes[6] !== 0
    || bytes[7] !== 0
  ) {
    throw new Error("invalid Wasm header");
  }
  const cursor = { value: 8 };
  let memory;
  while (cursor.value < bytes.byteLength) {
    const sectionId = bytes[cursor.value];
    cursor.value += 1;
    const sectionLength = readU32Leb(bytes, cursor, bytes.byteLength);
    const sectionEnd = cursor.value + sectionLength;
    if (!Number.isSafeInteger(sectionEnd) || sectionEnd > bytes.byteLength) {
      throw new Error("invalid Wasm section");
    }
    if (sectionId === 5) {
      if (memory !== undefined || readU32Leb(bytes, cursor, sectionEnd) !== 1) {
        throw new Error("expected one defined Wasm memory");
      }
      const flags = readU32Leb(bytes, cursor, sectionEnd);
      if ((flags & ~0x03) !== 0 || (flags & 1) === 0) {
        throw new Error("Wasm memory requires an explicit maximum");
      }
      const minimum = readU32Leb(bytes, cursor, sectionEnd);
      const maximum = readU32Leb(bytes, cursor, sectionEnd);
      if (
        cursor.value !== sectionEnd
        || minimum === 0
        || maximum !== MAX_WASM_PAGES
        || minimum > maximum
        || (flags & 2) !== 0
      ) {
        throw new Error("Wasm memory is not fixed to the 64MiB ceiling");
      }
      memory = Object.freeze({
        minimum,
        maximum,
        shared: false,
      });
    }
    cursor.value = sectionEnd;
  }
  if (memory === undefined) {
    throw new Error("missing defined Wasm memory");
  }
  return memory;
};

const sortedDescriptors = (descriptors, includeModule) =>
  descriptors
    .map((descriptor) => [
      includeModule ? descriptor.module : "",
      descriptor.name,
      descriptor.kind,
    ].join("\u0000"))
    .sort();

export const validateNativeWorkerModule = async (bytes) => {
  if (
    !(bytes instanceof Uint8Array)
    || bytes.byteLength === 0
    || bytes.byteLength > MAX_NATIVE_WORKER_ARTIFACT_BYTES
  ) {
    throw new Error("Native Worker artifact exceeded its hard byte cap");
  }
  const memory = inspectNativeWorkerMemory(bytes);
  inspectNativeWorkerAbi(bytes);
  const module = await WebAssembly.compile(bytes);
  const imports = WebAssembly.Module.imports(module);
  if (imports.length !== 0) {
    throw new Error("Native Worker Wasm must have zero imports");
  }
  const expectedExports = [
    { name: "memory", kind: "memory" },
    ...NATIVE_WORKER_EXPORTS
      .filter((name) => name !== "memory")
      .map((name) => ({
        name,
        kind: name.startsWith("__") ? "global" : "function",
      })),
  ];
  const actual = sortedDescriptors(
    WebAssembly.Module.exports(module),
    false,
  );
  const expected = sortedDescriptors(expectedExports, false);
  if (
    actual.length !== expected.length
    || actual.some((entry, index) => entry !== expected[index])
  ) {
    throw new Error("Native Worker Wasm export surface drifted");
  }
  const instance = await WebAssembly.instantiate(module, {});
  const wasmMemory = instance.exports.memory;
  if (
    !(wasmMemory instanceof WebAssembly.Memory)
    || wasmMemory.buffer.byteLength !== memory.minimum * WASM_PAGE_BYTES
    || (
      Object.getOwnPropertyDescriptor(
        ArrayBuffer.prototype,
        "resizable",
      ) !== undefined
      && wasmMemory.buffer.resizable !== false
    )
  ) {
    throw new Error("Native Worker instantiated an invalid memory backing");
  }
  const initialize = instance.exports.pdf_rs_worker_initialize;
  const dispatch = instance.exports.pdf_rs_worker_dispatch;
  const poll = instance.exports.pdf_rs_worker_poll;
  const epoch = instance.exports.pdf_rs_worker_memory_epoch;
  const shutdown = instance.exports.pdf_rs_worker_shutdown;
  const abiVersion = instance.exports.pdf_rs_worker_abi_version;
  const abiWords = NATIVE_WORKER_ABI_WORDS.map((_, index) =>
    instance.exports[`pdf_rs_worker_abi_hash_${index}`]);
  if (
    typeof initialize !== "function"
    || typeof dispatch !== "function"
    || dispatch(0, 0) !== NATIVE_WORKER_STATUS_REJECTED
    || typeof poll !== "function"
    || poll() !== NATIVE_WORKER_STATUS_REJECTED
    || typeof epoch !== "function"
    || epoch() !== 0
    || typeof abiVersion !== "function"
    || abiVersion() !== NATIVE_WORKER_ABI_VERSION
    || abiWords.some(
      (word, index) =>
        typeof word !== "function"
        || (word() >>> 0) !== NATIVE_WORKER_ABI_WORDS[index],
    )
    || typeof shutdown !== "function"
    || shutdown() !== NATIVE_WORKER_STATUS_REJECTED
  ) {
    throw new Error("Native Worker ABI pre-initialization proof failed");
  }
  return Object.freeze({ module, memory });
};
